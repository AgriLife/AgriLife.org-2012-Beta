		<p>Another unanswered question is why there are no sightings of young black tiger shrimp.
		</p>
		<p>“It could be because the young blend in with the native species and just can’t be distinguished,” he said. “There just needs to be more studies done.”
		</p>
		<p>Reisinger has met with the Brownsville-Port Isabel Shrimp Producers Association to discuss their concerns and share what he knows.
