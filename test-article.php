	<div class="entry-content">
		<p>SOUTH PADRE ISLAND  –  An exotic, large species of shrimp is being found once again in the Gulf of Mexico and posing a potential threat to the $700 million Gulf shrimping industry, according to Tony Reisinger, a Texas AgriLife Extension Service agent for coastal and marine resources in South Texas.</p>
		<p>“They’re known as black tiger shrimp because of their bright yellow stripes,” Reisinger said. “They are the biggest saltwater shrimp in the world. They are predatory, aggressive and could carry diseases that can harm native species of shrimp.”</p>
		<p><img class="aligncenter" src="<?php bloginfo('stylesheet_directory') ?>/images/boat.jpg" /></p>
		<p>Female shrimp are slightly larger than males and can grow to an average of about a foot in length and can weigh almost one pound, he said. They are native to the Indo-Pacific region of the world.
		“Black tiger shrimp eat the same type of food as our three, much smaller native species, but as they grow they can also eat the native species, which are the brown, white and pink shrimp. And they prey on small oysters, threatening that industry as well,” Reisinger said.</p>
		
		<p>“There are lots of unanswered questions about black tiger shrimp,” he said. “We know there was an accidental release of black tigers from a research facility in South Carolina in 1988. Some were caught as far away as St. Augustine, Fla. But most were thought to have been caught by local fishermen, because in 1991 they suddenly disappeared. Then in 2006 they started showing up again. These could have come from a shrimp farm in the Caribbean that was breached by a hurricane a few years ago, but we just don’t know.”
		</p>
		<p>Genetic studies are currently being conducted by the Texas Parks and Wildlife Department. Other organizations, including Texas A&amp;M’s Texas Sea Grant are also looking into the issue. Genetic studies are currently being conducted by the Texas Parks and Wildlife Department. Other organizations, including Texas A&amp;M’s Texas Sea Grant are also looking into the issue. “If we can figure out where they are coming from, if we find out for example that a foreign country is raising them in unsecured areas,, the United States could apply trading pressure to stop the practice. But who knows? At this point, there are just too many questions and stories.”
		</p>
